// This program prints size of all data types

#include <stdio.h>

int main() {
    int a;
    char b;
    float c;
    double d;
    printf("Siz of int is : %d\n", sizeof(a));
    printf("Siz of char is : %d\n", sizeof(b));
    printf("Siz of float is : %d\n", sizeof(c));
    printf("Siz of double is : %d\n", sizeof(d));
    return 0;
}
